CREATE OR REPLACE FUNCTION "public"."update_account_balance"() RETURNS "trigger"
    LANGUAGE "plpgsql"
    AS $$
BEGIN
  -- Handle Insert
  IF (TG_OP = 'INSERT') THEN
    UPDATE public.accounts
    SET current_balance_cents = current_balance_cents + NEW.amount_cents
    WHERE id = NEW.account_id;
  
  -- Handle Delete
  ELSIF (TG_OP = 'DELETE') THEN
    UPDATE public.accounts
    SET current_balance_cents = current_balance_cents - OLD.amount_cents
    WHERE id = OLD.account_id;
  END IF;
  RETURN NULL;
END;
$$;


