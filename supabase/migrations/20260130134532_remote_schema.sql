           FROM "public"."allocations" "a"
          WHERE ("a"."pay_period_id" = "pp"."id")), (0)::numeric) AS "total_allocated",
    COALESCE(( SELECT "sum"("a"."spent_amount_cents") AS "sum"
           FROM "public"."allocations" "a"
          WHERE ("a"."pay_period_id" = "pp"."id")), (0)::numeric) AS "total_spent"
   FROM "public"."pay_periods" "pp";


